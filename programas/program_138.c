#include <stdio.h>
#include <stdlib.h>

int main ( int argc , char * argv [] ) {
    int n ;
    float  f ;
    double d ;
    char  letra ;
    // leitura de int
    scanf ("%d",&n ) ;
    scanf ("%i",&n ) ;
    // leitura de char
    scanf ("%d", &letra ) ;
    scanf ("%c", &letra ) ;
    // leitura de float e double
    scanf ("%f", &f ) ;
    scanf ("%f", &d ) ;

    system ( "pause" ) ;
    return 0;
};
